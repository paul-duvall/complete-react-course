'use strict';

// Div that will contain the app
var appRoot = document.getElementById('app');
var app = {
  title: 'Indecision App',
  subtitle: 'The app for people who can\'t stand making decisions!',
  options: []
};

// Function to generate the options
function displayOptions(options) {
  // Only runs if there are items in the options array
  if (options.length > 0) {
    return React.createElement(
      'div',
      null,
      React.createElement(
        'p',
        null,
        'Here are your options:'
      ),
      React.createElement(
        'ul',
        null,
        React.createElement(
          'li',
          null,
          'Item 1'
        ),
        React.createElement(
          'li',
          null,
          'Item 2'
        )
      )
    );
  } else {
    return React.createElement(
      'p',
      null,
      'No options'
    );
  }
}

var onFormSubmit = function onFormSubmit(e) {
  e.preventDefault();
  var option = e.target.elements.option.value;

  if (option) {
    app.options.push(option);
    e.target.elements.option.value = '';
    renderIndecisionApp();
  }
};

var onRemoveAll = function onRemoveAll() {
  app.options = [];
  renderIndecisionApp();
};

var renderIndecisionApp = function renderIndecisionApp() {

  // JSX - JavaScript XML
  // This is a JavaScript extension included within React
  // Note that app.subtitle is only displayed if it exists - the first part of the statement must be truth
  // The options are generated by a separate function
  var template = React.createElement(
    'div',
    null,
    React.createElement(
      'h1',
      null,
      app.title
    ),
    app.subtitle && React.createElement(
      'p',
      null,
      app.subtitle
    ),
    React.createElement(
      'p',
      null,
      app.options.length
    ),
    React.createElement(
      'button',
      { onClick: onRemoveAll },
      'Remove All'
    ),
    displayOptions(app.options),
    React.createElement(
      'form',
      { onSubmit: onFormSubmit },
      React.createElement('input', { type: 'text', name: 'option' }),
      React.createElement(
        'button',
        null,
        'Add Option'
      )
    )
  );

  ReactDOM.render(template, appRoot);
};

renderIndecisionApp();
